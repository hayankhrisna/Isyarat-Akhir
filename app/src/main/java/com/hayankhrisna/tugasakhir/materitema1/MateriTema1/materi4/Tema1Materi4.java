package com.hayankhrisna.tugasakhir.materitema1.MateriTema1.materi4;

public class Tema1Materi4 {

    private String Title4;
    private int Thumbnail4;
    private String Video4;

    public Tema1Materi4(){

    }

    public Tema1Materi4(String title4, int thumbnail4, String video4) {

        Title4 = title4;
        Thumbnail4 = thumbnail4;
        Video4 = video4;
    }

    public String getTitle4() {

        return Title4;
    }

    public void setTitle4(String title4) {

        Title4 = title4;
    }

    public int getThumbnail4() {
        return Thumbnail4;
    }

    public void setThumbnail4(int thumbnail4) {
        Thumbnail4 = thumbnail4;
    }

    public String getVideo4() {
        return Video4;
    }

    public void setVideo4(String video4) {
        Video4 = video4;
    }
}