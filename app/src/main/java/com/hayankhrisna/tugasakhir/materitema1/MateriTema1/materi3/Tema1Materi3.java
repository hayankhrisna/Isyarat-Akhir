package com.hayankhrisna.tugasakhir.materitema1.MateriTema1.materi3;

public class Tema1Materi3 {

    private String Title3;
    private int Thumbnail3;
    private String Video3;

    public Tema1Materi3(){

    }

    public Tema1Materi3(String title3, int thumbnail3, String video3) {

        Title3 = title3;
        Thumbnail3 = thumbnail3;
        Video3 = video3;
    }

    public String getTitle3() {

        return Title3;
    }

    public void setTitle3(String title3) {

        Title3 = title3;
    }

    public int getThumbnail3() {
        return Thumbnail3;
    }

    public void setThumbnail3(int thumbnail3) {
        Thumbnail3 = thumbnail3;
    }

    public String getVideo3() {
        return Video3;
    }

    public void setVideo3(String video3) {
        Video3 = video3;
    }
}
