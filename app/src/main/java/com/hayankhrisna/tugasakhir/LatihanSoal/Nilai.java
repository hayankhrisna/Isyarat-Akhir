package com.hayankhrisna.tugasakhir.LatihanSoal;

public class Nilai {

    String nilai;
    String namalengkapNilai;

    public Nilai(){
    }

    public Nilai(String nilai, String namalengkapNilai){
        this.nilai = nilai;
        this.namalengkapNilai = namalengkapNilai;
    }

    public String getNilai() {
        return nilai;
    }

    public void setNilai(String nilai) {
        this.nilai = nilai;
    }

    public String getNamalengkapNilai() {
        return namalengkapNilai;
    }

    public void setNamalengkapNilai(String namalengkapNilai) {
        this.namalengkapNilai = namalengkapNilai;
    }




}
